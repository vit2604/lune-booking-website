import { readdir, stat, writeFile } from 'node:fs/promises';
import path from 'node:path';
import sharp from 'sharp';

const ROOT = path.resolve('public/images/lune');
const PUBLIC_ROOT = path.resolve('public');
const MANIFEST = path.resolve('src/utils/mobileImageManifest.js');
const MOBILE_WIDTH = 750;
const generated = [];

async function* walk(dir) {
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) yield* walk(full);
    else yield full;
  }
}

for await (const file of walk(ROOT)) {
  if (!/\.(webp|jpe?g|png)$/i.test(file) || /-mobile\.webp$/i.test(file)) continue;
  const meta = await sharp(file).metadata();
  if ((meta.width ?? 0) <= 900) continue;
  const out = file.replace(/\.(webp|jpe?g|png)$/i, '-mobile.webp');
  await sharp(file).resize({ width: MOBILE_WIDTH }).webp({ quality: 78 }).toFile(out);
  const { size } = await stat(out);
  generated.push('/' + path.relative(PUBLIC_ROOT, file).split(path.sep).join('/'));
  console.log(`${path.relative(ROOT, out)} ${(size / 1024).toFixed(0)} KB`);
}

generated.sort();
await writeFile(
  MANIFEST,
  `// Auto-generated by scripts/generate-mobile-images.mjs — do not edit by hand.\n` +
    `// Danh sach anh goc da co ban "-mobile.webp" 750px ben canh.\n` +
    `export const imagesWithMobileVariant = new Set(${JSON.stringify(generated, null, 2)});\n`,
);
console.log(`manifest: ${generated.length} entries -> src/utils/mobileImageManifest.js`);
